
public class Logical {

	public static void main(String[] args) {
		
		boolean m = true;
		boolean n = false;
		
//		Perform logical operator between m and n
		System.out.println(m&&n);
		System.out.println(n&&n);
		System.out.println(m&&m);
		System.out.println(m||n);
		System.out.println(n||n);
		System.out.println(m||n);

	}

}
